class Node:
    def __init__(self,data):
        self.right=self.left=None
        self.data = data
class Solution:
    def insert(self,root,data):
        if root==None:
            return Node(data)
        else:
            if data<=root.data:
                cur=self.insert(root.left,data)
                root.left=cur
            else:
                cur=self.insert(root.right,data)
                root.right=cur
        return root

    def getHeight(self,root):
        if root==None:
            return -1
        depth_left=self.getHeight(root.left)
        depth_right=self.getHeight(root.right)
        depth= max(depth_left,depth_right)+1
        return depth

